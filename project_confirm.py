def frames_extraction(video_path):
  '''
  This function will extract required frames from the video after resizing and normalizing them.
  Args:
    video_path: The path of the  video in the disk, whose frames are to be extracted.
  Returns:
    frames_list: A list containing the resized  and normalized frame of the video
  '''

  #Declaring frame list
  frame_list = []

  #Read the video using Video Capture object
  video_read = cv2.VideoCapture(video_path)

  #Counts the frmaes in the video
  vid_frame_count = int(video_read.get(cv2.CAP_PROP_FRAME_COUNT))

  #Calculates the interval after which frames will be added to the list
  sk_frame_count = max(int(vid_frame_count/SEQUENCE_LENGTH), 1)

  for frame_count in range(SEQUENCE_LENGTH):

    #Set current frame position of the video
    video_read.set(cv2.CAP_PROP_POS_FRAMES, frame_count * sk_frame_count)

    #Reading frame from the video
    success, frame = video_read.read()

    #Check if the video u=is not successfuly read
    if not success:
      break

    #Resize the frame
    resized_frame = cv2.resize(frame, (IMAGE_HEIGHT, IMAGE_WIDTH))

    #Normalize the frame by dividing it with 255 so that each pixel calue lies between 0 and 1
    normalized_frame = resized_frame/255

    #append the normalized frame to the frame list
    frame_list.append(normalized_frame)

  #Release the VidoeCapture Object
  video_read.release()

  return frame_list

def create_dataset():
  '''
  This function will extract the data from the selected class and create the required dataset
  Returns:
    features:         A list containing the extracted frames of the videos
    labels:           A list containing the indexes of the classes associated the videos
    vidoe_file_path:  A list containing the paths of the videos in the disk.
  '''

  #Createing the empty list
  features = []
  labels = []
  video_file_path = []

  for class_index, clas_name in enumerate(CLASSES_LIST):

    print(f'Extracting data of class: {clas_name}')

    #Get list of video files present in the specific class name directory.
    files_list = os.listdir(os.path.join(DATASET_DIR, clas_name))

    for file_name in files_list:

      #Get full vidoe path
      video_file_paths = os.path.join(DATASET_DIR, clas_name, file_name)

      #Exctract the frames from the video file
      frames = frames_extraction(video_file_paths)

      #Check if the extracted frames is equl to Sequece length
      #Ignore the video having frames less than Sequence Length
      if len(frames) == SEQUENCE_LENGTH:

        #Append the data according to the list created
        features.append(frames)
        labels.append(class_index)
        video_file_path.append(video_file_paths)

  features = np.asarray(features)
  labels = np.array(labels)

  return features, labels, video_file_path


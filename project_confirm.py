def plot_metric(model_training_history, metric_name_1, metric_name_2, plot_name):
  '''
  This function will plot the metrics passed to it in graph.
  Args:
    model_training_history: A history object containing a record of training and validation loss values and metrics value at successive epochs
    metric_name_1:          The name of the first metric that needs to be plotted in the graph .
    metric_name_2:          The name of the first metric that needs to be plotted in the graph .
    plot_name:              The title of the graph.
  '''

  metric_value_1 = model_training_history.history[metric_name_1]
  metric_value_2 = model_training_history.history[metric_name_2]

  epochs = range(len(metric_value_1))

  plt.plot(epochs, metric_value_1, 'blue', label = metric_name_1)
  plt.plot(epochs, metric_value_2, 'red', label = metric_name_2)

  plt.title(str(plot_name))

  plt.legend()

